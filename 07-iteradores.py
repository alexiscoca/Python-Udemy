# Lista con 6 elementos
lenguajes = ["Python", "Kotlin", "Java", "JavaScript", "PHP", "Go"]

# Iteradores
# Un iterador es un objeto que contiene un numero contable de valores
for lenguaje in lenguajes:
    print(f"Estoy aprendiendo {lenguaje}")

# For que escribe numeros
for numero in range(0, 10):
    print(numero)